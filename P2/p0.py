

import collections

de=collections.deque([1,2,3,4,6])

print("The newly created deque is ")

print(de)

#popping element from the list 

de.pop()

print("After poping  elements from deque ")

print(de)

# popping element from left

de.popleft()

print("After poping  elements from left in deque ")

print(de)
 
