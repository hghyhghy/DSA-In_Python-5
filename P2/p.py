
# the collections of python 

import collections

# initializing the deque

de=collections.deque([1,2,3])

print("The newly made deque is")

print(de)

# appending the new values 

de.append(4)

print("After appending the deque is ")

print(de)

# using appenleft

de.appendleft(6)

print("The deque after appending left")

print(de)